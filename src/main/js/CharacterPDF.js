    setOtherSkill(characterWeb, skillNameId, skillValueId) {
        let name = characterWeb.getTextFieldValue(skillNameId).replaceAll(/\s\d\d%:/g, "");
        console.log(skillNameId);
        console.log("name: " + characterWeb.getTextFieldValue(skillNameId));
        console.log("name regex: " + characterWeb.getTextFieldValue(skillNameId).replaceAll(/\s\d\d%:/g, ""))

        let subtype = name.substring(name.indexOf("(")+1, name.indexOf(")"))
        let value = characterWeb.getTextFieldValue(skillValueId)
        let skillNameKey = "";
        let skillValueKey = "";
        let otherSkillKeys = [
            [FOREIGN_LANGUAGES_AND_OTHER_SKILLS_ONE_KEY, FOREIGN_LANGUAGES_AND_OTHER_SKILLS_ONE_SCORE_KEY],
            [FOREIGN_LANGUAGES_AND_OTHER_SKILLS_TWO_KEY, FOREIGN_LANGUAGES_AND_OTHER_SKILLS_TWO_SCORE_KEY],
            [FOREIGN_LANGUAGES_AND_OTHER_SKILLS_THREE_KEY, FOREIGN_LANGUAGES_AND_OTHER_SKILLS_THREE_SCORE_KEY],
            [FOREIGN_LANGUAGES_AND_OTHER_SKILLS_FOUR_KEY, FOREIGN_LANGUAGES_AND_OTHER_SKILLS_FOUR_SCORE_KEY],
            [FOREIGN_LANGUAGES_AND_OTHER_SKILLS_FIVE_KEY, FOREIGN_LANGUAGES_AND_OTHER_SKILLS_FIVE_SCORE_KEY],
            [FOREIGN_LANGUAGES_AND_OTHER_SKILLS_SIX_KEY, FOREIGN_LANGUAGES_AND_OTHER_SKILLS_SIX_SCORE_KEY]
        ]
        for(let i = 0; i < otherSkillKeys.length; i ++) {
            console.log("Other skill " + i + ": " + this.getTextFieldValue(otherSkillKeys[i][0]));
            if(this.getTextFieldValue(otherSkillKeys[i][0]) === undefined) {
                skillNameKey = otherSkillKeys[i][0];
                skillValueKey = otherSkillKeys[i][1];
                break;
            }
        }
        console.log("skillNameKey " + skillNameKey);
        console.log("skillValueKey " + skillValueKey);
        if(name.toLowerCase().includes("science")) {
            let input = this.getScienceInput()
            if(input === undefined) {
                name = subtype
                skillNameKey = SCIENCE_INPUT_KEY
                skillValueKey = SCIENCE_KEY
            }
        }
        else if(name.toLowerCase().includes("art")) {
            let input = this.getArtInput()
            if(input === undefined) {
                name = subtype
                skillNameKey = ART_INPUT_KEY
                skillValueKey = ART_KEY
            }
        }
        else if(name.toLowerCase().includes("military science")) {
            let input = this.getMilitaryScienceInput()
            if(input === undefined) {
                name = subtype
                skillNameKey = MILITARY_SCIENCE_INPUT_KEY
                skillValueKey = MILITARY_SCIENCE_KEY
            }
        }
        else if(name.toLowerCase().includes("pilot")) {
            let input = this.getPilotInput()
            if(input === undefined) {
                name = subtype
                skillNameKey = PILOT_INPUT_KEY
                skillValueKey = PILOT_KEY
            }
        }
        else if(name.toLowerCase().includes("craft")) {
            let input = this.getCraftInput()
            if(input === undefined) {
                name = subtype
                skillNameKey = CRAFT_INPUT_KEY
                skillValueKey = CRAFT_KEY
            }
        }
        this.setTextField(skillNameKey, name)
        this.setTextField(skillValueKey, value)
    }

    async download() {
        const data = await this.doc.save()
        let filename = getName() + ".pdf";
        let type = "pdf";
        // Function to download data to a file
        var file = new Blob([data], {type: type});
        if (window.navigator.msSaveOrOpenBlob) // IE10+
            window.navigator.msSaveOrOpenBlob(file, filename);
        else { // Others
            var a = document.createElement("a"),
                url = URL.createObjectURL(file);
            a.href = url;
            a.download = filename;
            document.body.appendChild(a);
            a.click();
            setTimeout(function () {
                this.document.body.removeChild(a);
                window.URL.revokeObjectURL(url);
            }, 0);
        }
    }

